							<h3>Mereka juga punya kelebihan yang patut dan layak dikembangkan.</h3>
							<p>Anak Broken Home merupakan korban dari ketidak harmonisan sebuah keluarga. University of New Hampshire Cooperative Extension menjelaskan bahwa efek dari keluarga yang tidak harmonis pada perkembangan anak tergantung pada berbagai faktor, termasuk usia seorang anak ketika orangtua bercerai, kepribadian anak, dan hubungan di dalam keluarga. Tumbuh dan berkembang dalam keluarga yang tidak harmonis dapat menyebabkan psikologis si anak terganggu. Anak-anak broken home akan lebih mengerti apa itu arti kehilangan sesungguhnya,terkadan mereka juga merasa dunia tidak adil. </p>
<p>Banyak orang yang berspekulasi bahwa anak-anak yang berasa dari keluarga tidak utuh akan menjadi pribadi nakal,suka menyendiri,tidak bisa berekspresi dengan baik,dan tidak bisa sukses seperti yang lainnya. Semua itu salah karna justru anak-anak broken home memiliki kelebihan yang tidak dimiliki anak-anak normal lainnya.</p>
<p>Misalnya karena semua kejadian yang dialami semenjak kecil tidak sedikit dari mereka yang menjadi dewasa sebelum waktunya. Ketidakhadiran salah satu orang tua, atau bahkan kedua orang tuanya membuatnya sangat mandiri. Sehingga dia sangat menyadari, jika dia tidak mampu melakukannya sendiri siapa lagi yang akan membantunya. Mereka juga memiliki mental yang tangguh karna biasanya mereka sudah biasa menerima hinaan dan caciaan semenjak kecil. Karna hal itu ,anak broken home biasanya memiliki motivasi sendiri untuk hidup lebih baik dan menunjukan diri kepada orang-orang bahwa mereka bisa sukses seperti yang lainnya. </p>
 
</p>Banyak sekali orang-orang sukses yang berasal dari keluarga broken home. Contoh saja artis dunia yang terkenal lewat youtube yaitu Justin Bieber. Bieber dibesarkan oleh ibunya seorang. Namun dia tumbuh menjadi seorang anak yang aktif dan bisa membanggakan ibunya dengan kesuksesannya sekarang. </p>
 
<p>Siapa yang sangka mantan orang nomor 1 di Indonesia Pak Susilo Bambang Yudhoyono termasuk salah satu korban anak broken home. Orang tua beliau bercerai saat beliau masih remaja. Sempat merasa terguncang akibat perceraian orang tuanya,beliau bertekad untuk mengubah hidupnya sambil berkata “ di persimpangan itu saya bersumpah harus keluar dari situasi broken home dan menjadi seseorang”. Kata-kata itu pun terbukti dengan beliau menjadi presiden RI selama 2 periode.</p>
<p>Jadi walaupun orang tua kalian bercerai atau merasa kelurga kalian tidak seperti dulu,itu bukan akhir dari segalanya. Angkat bahu kalian dan buktikan kepada semua orang bahwa kalian akan hidup lebih baik dari orang-orang diluar sana walaupun kalian bukan berasal dari keluarga yang utuh.</p>

